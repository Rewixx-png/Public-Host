# --- –£—Å—Ç–∞–Ω–æ–≤—â–∏–∫ Public-Host Bot —Å PM2 (–ì–ª–æ–±–∞–ª—å–Ω–∞—è —É—Å—Ç–∞–Ω–æ–≤–∫–∞) ---

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
GREEN='\033[0;32m'
