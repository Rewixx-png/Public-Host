    echo "" >&2
    echo -e "–î–ª—è —Ä–∞–±–æ—Ç—ã —ç—Ç–æ–≥–æ –±–æ—Ç–∞ —Ç—Ä–µ–±—É–µ—Ç—Å—è –ø–æ–ª–Ω–æ—Ü–µ–Ω–Ω–∞—è Linux-—Å–∏—Å—Ç–µ–º–∞" >&2
    echo -e "—Å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å—é —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –∏ –∑–∞–ø—É—Å–∫–∞ Docker." >&2
    echo -e "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ VPS, VDS –∏–ª–∏ –≤—ã–¥–µ–ª–µ–Ω–Ω—ã–π —Å–µ—Ä–≤–µ—Ä." >&2
    echo -e "============================================================" >&2
    # –í—ã—Ö–æ–¥ —Å –∫–æ–¥–æ–º –æ—à–∏–±–∫–∏
    exit 1
}

echo "–ü—Ä–æ–≤–æ–∂—É –ø—Ä–æ–≤–µ—Ä–∫—É —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏ –æ–∫—Ä—É–∂–µ–Ω–∏—è..."

# 1. –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ Termux –ø–æ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–ª–∏ –Ω–∞–ª–∏—á–∏—é –ø–∞–ø–∫–∏
if [ -n "$TERMUX_VERSION" ] || [ -d "/data/data/com.termux" ]; then
    fail_with_error "${YELLOW}–û–±–Ω–∞—Ä—É–∂–µ–Ω Termux. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –Ω–µ–≤–æ–∑–º–æ–∂–Ω–∞, —Ç–∞–∫ –∫–∞–∫ Docker –∑–¥–µ—Å—å –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω.${NC}"
fi

# 2. –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ proot (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤ UserLAnd, PRoot Distro –∏ —Ç.–¥.)
# [[ ]] - —ç—Ç–æ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏—è bash, –æ–Ω–∞ –±–æ–ª–µ–µ –Ω–∞–¥–µ–∂–Ω–∞ –¥–ª—è —Å–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω–∏—è —Å –æ–±—Ä–∞–∑—Ü–æ–º
if [[ "$LD_PRELOAD" == *proot* ]]; then
    fail_with_error "${YELLOW}–û–±–Ω–∞—Ä—É–∂–µ–Ω–∞ —Å—Ä–µ–¥–∞ –Ω–∞ –±–∞–∑–µ proot (–Ω–∞–ø—Ä–∏–º–µ—Ä, UserLAnd). –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –Ω–µ–≤–æ–∑–º–æ–∂–Ω–∞.${NC}"
fi

# 3. –û–±—â–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ Android –ø–æ –Ω–∞–ª–∏—á–∏—é —Å–∏—Å—Ç–µ–º–Ω–æ–≥–æ —Ñ–∞–π–ª–∞
if [ -e "/system/bin/app_process" ]; then
    fail_with_error "${YELLOW}–û–±–Ω–∞—Ä—É–∂–µ–Ω–∞ —Å—Ä–µ–¥–∞ Android. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –Ω–∞ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã—Ö Android-—É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞—Ö –Ω–µ–≤–æ–∑–º–æ–∂–Ω–∞.${NC}"
fi

echo -e "${GREEN}‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –æ–∫—Ä—É–∂–µ–Ω–∏—è –ø—Ä–æ–π–¥–µ–Ω–∞. –ü—Ä–æ–¥–æ–ª–∂–∞—é —É—Å—Ç–∞–Ω–æ–≤–∫—É...${NC}"
# --- –ö–û–ù–ï–¶ –ü–†–û–í–ï–†–ö–ò –û–ö–†–£–ñ–ï–ù–ò–Ø ---


# --- –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ ---
check_python() {
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ python3 –∏ pip3
    if ! command -v python3 &> /dev/null || ! command -v pip3 &> /dev/null
    then
        echo -e "${YELLOW}Python 3 –∏–ª–∏ pip –Ω–µ –Ω–∞–π–¥–µ–Ω—ã. –ü—ã—Ç–∞—é—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å...${NC}"
        sudo apt-get update
        sudo apt-get install -y python3 python3-pip
        if ! command -v python3 &> /dev/null; then
            echo -e "${YELLOW}–£—Å—Ç–∞–Ω–æ–≤–∫–∞ Python –Ω–µ —É–¥–∞–ª–∞—Å—å. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –µ–≥–æ –≤—Ä—É—á–Ω—É—é ('sudo apt install python3 python3-pip') –∏ –∑–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç —Å–Ω–æ–≤–∞.${NC}"
            exit 1
        fi
    else
        echo -e "${GREEN}Python 3 –∏ pip —É–∂–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã.${NC}"
    fi
}

check_and_install_pm2() {
    if ! command -v node &> /dev/null; then
        echo -e "${YELLOW}Node.js –Ω–µ –Ω–∞–π–¥–µ–Ω. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é Node.js (LTS)...${NC}"
        curl -sL https://deb.nodesource.com/setup_18.x | sudo -E bash -
        sudo apt-get install -y nodejs
        if ! command -v node &> /dev/null; then
            echo -e "${YELLOW}–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å Node.js. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –≤—Ä—É—á–Ω—É—é –∏ –ø–æ–≤—Ç–æ—Ä–∏—Ç–µ.${NC}"
            exit 1
        fi
    else
        echo -e "${GREEN}Node.js —É–∂–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω.${NC}"
    fi

    if ! command -v pm2 &> /dev/null; then
        echo -e "${YELLOW}PM2 –Ω–µ –Ω–∞–π–¥–µ–Ω. –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é PM2 –≥–ª–æ–±–∞–ª—å–Ω–æ...${NC}"
        sudo npm install pm2 -g
        if ! command -v pm2 &> /dev/null; then
            echo -e "${YELLOW}–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å PM2. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –≤—Ä—É—á–Ω—É—é ('sudo npm install pm2 -g') –∏ –ø–æ–≤—Ç–æ—Ä–∏—Ç–µ.${NC}"
            exit 1
        fi
    else
        echo -e "${GREEN}PM2 —É–∂–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω.${NC}"
    fi
}

# --- –û—Å–Ω–æ–≤–Ω–æ–π –ø—Ä–æ—Ü–µ—Å—Å —É—Å—Ç–∞–Ω–æ–≤–∫–∏ ---
echo -e "\n--- ${BLUE}–®–∞–≥ 1: –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–∏—Å—Ç–µ–º–Ω—ã—Ö –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π${NC} ---"
