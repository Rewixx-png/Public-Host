echo -e "${BLUE}–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ —É—Å—Ç–∞–Ω–æ–≤—â–∏–∫ Public-Host Bot!${NC}"
echo "–≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –Ω–∞—Å—Ç—Ä–æ–∏—Ç –æ–∫—Ä—É–∂–µ–Ω–∏–µ –∏ –∑–∞–ø—É—Å—Ç–∏—Ç –±–æ—Ç–∞ –∫–∞–∫ —Å–µ—Ä–≤–∏—Å."

# --- –ù–ê–ß–ê–õ–û –ü–†–û–í–ï–†–ö–ò –û–ö–†–£–ñ–ï–ù–ò–Ø ---
# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ –∫—Ä–∞—Å–∏–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ –∏ –≤—ã—Ö–æ–¥–∞ –∏–∑ —Å–∫—Ä–∏–ø—Ç–∞
fail_with_error() {
    # –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –ø–æ—Ç–æ–∫ –æ—à–∏–±–æ–∫ (stderr)
    echo -e "\n============================================================" >&2
    echo -e "!!! ${YELLOW}–ö–†–ò–¢–ò–ß–ï–°–ö–ê–Ø –û–®–ò–ë–ö–ê –£–°–¢–ê–ù–û–í–ö–ò${NC} !!!" >&2
    echo "" >&2
    # –í—ã–≤–æ–¥–∏–º –∫–æ–Ω–∫—Ä–µ—Ç–Ω—É—é –ø—Ä–∏—á–∏–Ω—É, –ø–µ—Ä–µ–¥–∞–Ω–Ω—É—é –≤ —Ñ—É–Ω–∫—Ü–∏—é
