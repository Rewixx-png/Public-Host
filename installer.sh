echo -e "\n–ü–æ—Å–ª–µ —ç—Ç–æ–≥–æ —Å–∫–æ–ø–∏—Ä—É–π—Ç–µ –∏ –≤—ã–ø–æ–ª–Ω–∏—Ç–µ —Ç—É –∫–æ–º–∞–Ω–¥—É, –∫–æ—Ç–æ—Ä—É—é –≤–∞–º –ø–æ–∫–∞–∂–µ—Ç."