echo -e "–í–∞—à –±–æ—Ç —Ç–µ–ø–µ—Ä—å —Ä–∞–±–æ—Ç–∞–µ—Ç –≤ —Ñ–æ–Ω–æ–≤–æ–º —Ä–µ–∂–∏–º–µ –ø–æ–¥ —É–ø—Ä–∞–≤–ª–µ–Ω–∏–µ–º PM2."

echo -e "\n${YELLOW}--- –ü–æ–ª–µ–∑–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã PM2 ---${NC}"
echo -e "–ü—Ä–æ—Å–º–æ—Ç—Ä —Å—Ç–∞—Ç—É—Å–∞ –≤—Å–µ—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤: ${GREEN}pm2 list${NC}"
echo -e "–ü—Ä–æ—Å–º–æ—Ç—Ä –ª–æ–≥–æ–≤ –≤–∞—à–µ–≥–æ –±–æ—Ç–∞:      ${GREEN}pm2 logs Public-Host${NC}"
echo -e "–ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å –±–æ—Ç–∞:              ${GREEN}pm2 restart Public-Host${NC}"
echo -e "–û—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –±–æ—Ç–∞:                 ${GREEN}pm2 stop Public-Host${NC}"

echo -e "\n${YELLOW}--- –í–ê–ñ–ù–û: –ê–≤—Ç–æ–∑–∞–ø—É—Å–∫ –ø–æ—Å–ª–µ –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∏ —Å–µ—Ä–≤–µ—Ä–∞ ---${NC}"
echo "–ß—Ç–æ–±—ã –≤–∞—à –±–æ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–ø—É—Å–∫–∞–ª—Å—è –ø–æ—Å–ª–µ –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∏ —Å–µ—Ä–≤–µ—Ä–∞, –≤—ã–ø–æ–ª–Ω–∏—Ç–µ —Å–ª–µ–¥—É—é—â—É—é –∫–æ–º–∞–Ω–¥—É."
echo "–°–∫–æ–ø–∏—Ä—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É, –∫–æ—Ç–æ—Ä—É—é –≤—ã–≤–µ–¥–µ—Ç ${GREEN}pm2 startup${NC}, –∏ –≤—ã–ø–æ–ª–Ω–∏—Ç–µ –µ–µ –æ—Ç –∏–º–µ–Ω–∏ —Å—É–ø–µ—Ä–ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (root)."
echo -e "\n–í—ã–ø–æ–ª–Ω–∏—Ç–µ —ç—Ç–æ —Å–µ–π—á–∞—Å:"
